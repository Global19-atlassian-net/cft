package org.eclipse.cft.server.core.internal.debug;

public class DebugConnectionDescriptor {

	private final String ip;

	private final int port;

	public DebugConnectionDescriptor(String ip, int port) {

		this.ip = ip;
		this.port = port;
	}

	public String getIp() {
		return ip;
	}

	public int getPort() {
		return port;
	}

	public boolean areValidIPandPort() {
		return ip != null && ip.length() > 0 && port > 0;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((ip == null) ? 0 : ip.hashCode());
		result = prime * result + port;
		return result;
	}

	@Override
	public String toString() {
		return "DebugConnectionDescriptor [ip=" + ip + ", port=" + port + "]";
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DebugConnectionDescriptor other = (DebugConnectionDescriptor) obj;
		if (ip == null) {
			if (other.ip != null)
				return false;
		}
		else if (!ip.equals(other.ip))
			return false;
		if (port != other.port)
			return false;
		return true;
	}

}